function genPlatforms(level, worldWidth) {
  const rng = (seed) => { let s = seed; return () => { s = (s * 9301 + 49297) % 233280; return s / 233280; }; };
  const r = rng(level * 137 + 42);
  const count = Math.min(6 + Math.floor(level * 0.8), 22);
  const platforms = [];
  let lastX = 200;
  for (let i = 0; i < count; i++) {
    const gap = Math.max(40, 130 - level * 1.5);
    const x = lastX + gap + Math.floor(r() * 180);
    const surfaceY = 200 + Math.floor(r() * 260);
    const tiles = 1 + Math.floor(r() * 4);
    if (x + tiles * 64 < worldWidth - 200) {
      platforms.push({ x, surfaceY, tiles });
      lastX = x + tiles * 64;
    }
  }
  return platforms;
}

function genEnemies(level, worldWidth) {
  const types = level < 35 ? ['slime','bat','ghost'] :
                level < 40 ? ['bat','ghost','shooter','shield'] :
                level < 45 ? ['shooter','shield','flier'] :
                             ['shooter','shield','flier','ghost'];
  const count = Math.min(8 + Math.floor(level * 0.6), 22);
  const enemies = [];
  for (let i = 0; i < count; i++) {
    enemies.push({
      type: types[Math.floor(Math.random() * types.length)],
      x: 300 + Math.floor(Math.random() * (worldWidth - 500)),
      y: 508
    });
  }
  return enemies;
}

